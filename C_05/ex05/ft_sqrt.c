int	ft_sqrt(int nb)
{
	int	a;
	int	b;

	a = 0;
	b = (nb / 2);
	if (nb == 1)
		return (1);
	if (nb < 1)
		return (0);
	while (b != a)
	{
		a = b;
		b = (nb / a + a) / 2;
	}
	if (b * b != nb)
		return (0);
	return (b);
}

