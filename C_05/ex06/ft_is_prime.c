int	ft_is_prime(int nb)
{
	int	d;

	d = 3;
	if (nb <= 1)
		return (0);
	if (nb % 2 == 0)
		return (nb == 2);
	while ((d * d <= nb) && (nb % d != 0))
		d += 2;
	return (d * d > nb);
}
