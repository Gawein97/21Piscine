#include <unistd.h>

void	ft_print_comb(void)
{
	char	tb[3];

	tb[0] = '0';
	tb[1] = '1';
	tb[2] = '2';
	while (tb[0] <= '7')
	{
		while (tb[1] <= '8')
		{
			while (tb[2] <= '9')
			{
				write(1, tb, 3);
				if (tb[0] != '7')
					write(1, ", ", 2);
				tb[2]++;
			}
			tb[1]++;
			tb[2] = tb[1] + 1;
		}
		tb[0]++;
		tb[1] = tb[0];
	}
}
